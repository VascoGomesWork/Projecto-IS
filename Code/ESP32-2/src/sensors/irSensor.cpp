/*
    This function detects, through a infrared proximity
    sensor, if an object is near. 
    Return - true if there is an object near and false
    if don't.
*/
int hasObjectIrSensor(IrSensor irSensor) {
    int state = digitalRead(irSensor.pin);

    if (state == LOW)
        return 1;

    return 0;
}

